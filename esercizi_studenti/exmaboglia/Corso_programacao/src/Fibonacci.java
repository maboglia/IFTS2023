
public class Fibonacci {
	public static void main(String[] args) {
		// Fibonacci - { 0,1,1,2,3,5,8,13,21...}
		
		
		double x = 0;
		double y = 1;
		
		for(int i = 0; i<=99; i++) {
			System.out.print(x + " ");
			double z = x+y;
			x = y;
			y = z;
			}
		
			
			
			
		}
		
		
		
		
		
		
		
	}


