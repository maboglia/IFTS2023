/*
 * Scrivere un programma che stampi a video i primi dieci 
 * interi pari compresi fra 20 e 0, partendo da 20.
 */


public class InteriDi20a0 {
	
	public static void main(String[] args) {
		
		int n = 0;
	for(int i=20; i>n; i--) {
		if(i%2 == 0 ) {
		System.out.print(i + " ");
		}
		
	}
	}
}

	

