import java.util.Scanner;

/*
 * Scrivere un programma che stampi la tabellina del numero dato come argomento
 */

public class Tabellina {

	
	public static void main(String[] args) {
		
		
		int n = 5;
				
		for(int i=1; i<=10; i++) {
			System.out.print(n * i + " ");
		}
		
	}
}
